#!/bin/bash 

# The variable syntax must be uppercase and there cannot be a space before and after the equals sign. 
# To print the variable, use the '$' then variable name. 
NAME="Inwoo"
echo "My name is $NAME!"

# To print the variable in conjunction with a string, use '{}'.
SPORT="Foot"
echo "The most popular sport is ${SPORT}ball."

